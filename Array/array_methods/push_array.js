// push method will insert array element in last index of array
const array_1=[1,2,4,6,8,9];
array_1.push(5);
console.log(array_1,"after add 5");


// pop method will remove last element of array
console.log(array_1.pop(),"removed");
console.log(array_1,"after remove");

//unshift method will insert array element in 0 index of array
console.log(array_1.unshift(7),"add in array");
console.log(array_1,"after add element");

// shift method will remove 0 index element of array
console.log(array_1.shift(),"remove from array");
console.log(array_1,"after remove element");
